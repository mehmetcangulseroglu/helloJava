public class ifVeElseBlokları {
    public static void main(String args[]){
        int sayi1=50;
        int sayi2=51;
        if(sayi1>sayi2){
            System.out.println(sayi1+" büyüktür "+ sayi2 +" den");
        }
        else if(sayi1<sayi2){
            System.out.println(sayi1+" küçüktür "+ sayi2+" den");
        }
        else{
            System.out.println(sayi1+" eşittir "+ sayi2+" ye");
        }


    }


    }

